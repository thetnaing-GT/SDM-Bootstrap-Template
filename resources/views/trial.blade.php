        let map, infoWindow;
        let markers = [];
        let createFatmarkers = [];

        function initMap(lat = null, lng = null, id = "map") {
            if (id == "map") {
                markers = [];
            } else {
                createFatmarkers = [];
            }
            infoWindow = new google.maps.InfoWindow();

            map = new google.maps.Map(document.getElementById(id), {
                zoom: 18,
            });

            infoWindow.open(map);

            navigator.geolocation.getCurrentPosition(
                function(position) {
                    let pos;
                    if (lat && lng) {
                        pos = {
                            lat: parseFloat(lat),
                            lng: parseFloat(lng),
                        };
                    } else {
                        pos = {
                            lat: position.coords.latitude,
                            lng: position.coords.longitude,
                        };
                    }

                    // console.log(pos, 'pos')

                    infoWindow.open(map);
                    map.setCenter(pos);
                    const startpos = new google.maps.Marker({
                        position: pos,
                        map: map,
                        zoom: 18,
                    });

                    if (id == "map") {
                        markers.push(startpos);
                    } else {
                        createFatmarkers.push(startpos);
                    }

                    // console.log(pos);
                },
                function() {
                    centerMap(16.9239, 96.227);
                }
            );

            const centerMap = (lat, lng) => {
                const loc = {
                    lat: 16.9239,
                    lng: 96.227,
                };
                map.setCenter(loc);
            };

            map.addListener("click", function(event) {
                const loc = {
                    lat: event.latLng.lat(),
                    lng: event.latLng.lng(),
                };
                map.setCenter(loc);
                setMapOnAll(null);

                const marker = new google.maps.Marker({
                    position: {
                        lat: event.latLng.lat(),
                        lng: event.latLng.lng(),
                    },
                    map: map,
                });
                if (id == "map") {
                    markers.push(marker);
                    $("#lat").val(event.latLng.lat().toString().slice(0, 11));

                    $("#lng").val(event.latLng.lng().toString().slice(0, 11));

                    $("#latErrAlert").css("display", "none");
                    $("#lngErrAlert").css("display", "none");
                } else {
                    createFatmarkers.push(marker);
                    $("#fdtLat").val(event.latLng.lat().toString().slice(0, 11));

                    $("#fdtLng").val(event.latLng.lng().toString().slice(0, 11));

